#include <math.h>
#include <stdio.h>
#include "pmods.h"
#include "adc.h"
#include "lcd.h"


int global_data;

double tension;
int Vo;
float R1 = 1; //thermistor = 1 ohm
float logR2, R2, T;
float c1 = 1.009249522e-03, c2 = 2.378405444e-04, c3 = 2.019202697e-07;
char message[80] = "";
char msg[80] = "";

double analogread() {
   
 
    tension = ADC_AnalogRead(16);

    sprintf(msg, "V:%f", tension);
    LCD_WriteStringAtPos(msg, 0, 0);
    return tension;

}

void thermistor() {
   
    LCD_Init();
    Vo = 0;
    //Vo = analogread();
    LCD_DisplayClear();
    R2 = R1 * (1023.0 / (float)Vo - 1.0);
    logR2 = log(R2);
    T = (1.0 / (c1 + c2*logR2 + c3*logR2*logR2*logR2));
    T = T - 273.15;
    T = (T * 9.0)/ 5.0 + 32.0; 

    sprintf(message, "Temp:%f", T);
    //LCD_DisplayClear();
    LCD_WriteStringAtPos(message, 0, 0);
  
    //Serial.print("Temperature: "); 
    //Serial.print(T);
    //Serial.println(" F"); 

  
}

